                                <h2 class="mb-5 text-center"><b class="text-info">Recherches et developpement</b></h2>
                                <p>Le LPAOSF oeuvre aussi dans le Bilan hydrique (pour application à l’agriculture et ressources en eau)
                                Climat et santé : paludisme, émergence de la Fièvre de la Vallée du Rift ;
                                Dynamique des MCS (aide à la navigation aérienne)
                                Prévision numérique (navigation aérienne, agriculture)
                                Développement de scénarios de changements climatique (aide à la décision).</p>    
                                <div class="container">
                                    <div class=" mt-5 mb-5  pt-5 pb-5">
                                        <p class="text-justify">
                                            <img src="images/oceangif.gif" style="float: left; width: 480px; border-radius: 0; height: auto; margin: 0 15px 15px 0" alt="">
                                            <h4 class="text-center">Océanographie</h4>
                                            La compréhension du fonctionnement et de l'évolution des écosystèmes marins dans un contexte de changement
                                            climatique est un enjeu majeur pour notre société. La circulation océanique est au cœur des problématiques
                                            de sécurité qui sont liées aux courants marins et à la dispersion de traceurs. L’océan côtier est le siège
                                            d’une intense activité de pêche artisanale et industrielle et plus généralement de fortes pressions anthropiques
                                            ce qui pose des inquiétudes sur son devenir, et celui de son écosystème. L’activité scientifique de l’axe
                                            de recherche consiste à améliorer notre compréhension de l’évolution actuelle et future de l’environnement marin
                                            sénégalais à travers une mise en place d’un réseau d’observatoire régional dans la zone d’Upwelling (bouée, campagnes, 
                                            gliders etc.) ; et l’utilisation des produits satellites et de la modélisation numérique de l’hydrodynamique de l'océan 
                                            côtier à petite et moyenne échelle sur les échanges de matière et d’énergie, sur le fonctionnement des communautés biologiques
                                            ainsi que sur l’impact de la pollution marine sur les ressources halieutiques (petits poissons pélagiques et ressources 
                                            benthiques côtières).
                                        </p>
                                    </div>
                                    <div class=" mt-5 mb-5 pt-5 pb-5" >
                                        <p class="text-justify">
                                            <img src="images/atmospheregif.gif" style="float: right; width: 480px; border-radius: 0; height: 400px; margin: 0 15px 15px 0" alt="">
                                            <h4 class="text-center">Sciences de l'atmosphère</h4>
                                            Le climat au Sénégal et sur l’ensemble de l’Afrique de l’Ouest est principalement contrôlé par le cycle annuel du système de mousson africaine, système à la fois complexe et multi-échelle. Les pluies de mousson en Afrique de l’Ouest sont un élément essentiel pour les populations africaines puisqu’elles constituent souvent l’unique apport d’eau pour le secteur agricole de ces régions. Les grandes sécheresses des années soixante-dix qui ont provoqué une famine dramatique et où celles des années quatre-vingt ont décimé la quasi-totalité des troupeaux (PNUD 2004). Les récentes inondations de la décennie 2000 ont montré une fois de plus combien les populations ouest africaines sont vulnérables face aux aléas du régime pluviométrique. Les variations climatiques et environnementales influent également sur la dynamique de fléaux meurtriers en Afrique de l‘Ouest comme la méningite qui affecte entre 25 000 et 200 000 personnes par an et le paludisme responsable de 90% des décès d'enfants de moins de 5 ans en Afrique (Sultan, 2011). </p>
                                    </div>
                                    <div class=" mt-5 mb-5  pt-5 pb-5">
                                        <p class="text-justify">
                                            <img src="images/climatgif.gif" style="float: left; width: 480px; border-radius: 0; height: auto; margin: 0 15px 15px 0" alt="">
                                            <h4  class="text-center">Climat et impacts climatiques</h4>
                                            En raison de leurs répercussions immédiates et durables sur le milieu naturel et sur l’homme, les questions de variabilité et de changement climatiques sont placées depuis quelques temps au centre des préoccupations des scientifiques et des décideurs politiques dans le monde. Cet axe va traiter les bases scientifiques de certains modes de variabilité climatiqueq (MJO, ENSO, NOA, mode équatorial et inter-hémisphérique etc) et leurs influences sur la mousson Ouest Africaine, les Upwelling (zones riches en ressources halieutiques). L’axe prendra aussi en compte les notions de changement climatiques et leurs impacts positifs ou négatifs sur différents secteurs tels que les l’agriculture, la zone côtière, la santé et les inondations. Après l’identification des impacts du CC sur ces secteurs, nous traiterons leur vulnérabilité en évoquant la capité d’atténuation. Une fois que la vulnérabilité est établie, nous aborderons la gestion des risques en explorant les options d’atténuation et d’adaptation face au changement climatique. </p>
                                    </div>
